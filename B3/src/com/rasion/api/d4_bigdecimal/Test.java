package com.rasion.api.d4_bigdecimal;

public class Test {
    public static void main(String[] args) {
        // 目标：掌握BigDecimal的使用。
//        二进制
//        00000000010   --->2
//        00000000110   --->6
//        整数的表示 是 2的几次方
//        浮点型表示  2的-几次方
//         2的-1次方  1/2  ---->0.5
//         2的-2次方  1/4  ---->0.25
        System.out.println(0.1 + 0.2);
        System.out.println(1.0 - 0.32);
        System.out.println(1.015 * 100);
        System.out.println(1.301 / 100);
    }
}















